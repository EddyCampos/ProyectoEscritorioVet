    //Click a un elemento de a tabla
    private void tblExpedientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblExpedientesMouseClicked
        //Llamar a obtener un expediente de la logica        
        try {
            LExpedientes logica = new LExpedientes();
            ExpedientesMascotas expediente;
            String condicion;

            //Si da dos clicks
            if (evt.getClickCount() == 2) {
                //Desactiva boton buscar cliente
                btnBuscarCliente.setEnabled(false);

                //Obtiene toda la fila
                int fila = tblExpedientes.rowAtPoint(evt.getPoint());
                txtCodigo.setText(tblExpedientes.getValueAt(fila, 0).toString());//Obtiene de la tabla
                
                condicion = String.format("ID_EXPEDIENTE= %s", txtCodigo.getText());
                expediente = logica.ObtenerCliente(condicion);//Obtiene del cliente
                
                 //Convierte la fecha de date a localDate
                java.sql.Date fechaBaseDatos = java.sql.Date.valueOf(expediente.getFechaNacimiento());
                
                txtCodigo.setText(String.valueOf(expediente.getIdExpediente()));
                txtCodigoCliente.setText(String.valueOf(expediente.getIdCliente()));
                txtNombre.setText(expediente.getNombre());
                txtEspecie.setText(expediente.getEspecie());
                txtRaza.setText(expediente.getRaza());
                txtPeso.setText(String.valueOf(expediente.getPeso()));
                ddlSexo.setSelectedItem(expediente.getSexo());
                dtpFNacimiento.setDate(fechaBaseDatos);
            }
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        }
    }//GEN-LAST:event_tblExpedientesMouseClicked

    //Click boton buscar cliente
    private void btnBuscarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarClienteActionPerformed
        frmBuscarClientes buscarClientes = new frmBuscarClientes(null, true);
        
        //Cuando se cierra recupera el id
        buscarClientes.addWindowListener(new WindowAdapter(){
            @Override
            public void windowClosed(WindowEvent e){
                try {
                    int id = buscarClientes.ObtenerId();
                    
                    if (id > -1) {
                        txtCodigoCliente.setText(String.valueOf(id));
                    }
                    else{
                        LimpiarCasillas();
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
                }
            }
        
        });
        
        buscarClientes.setVisible(true);
    }//GEN-LAST:event_btnBuscarClienteActionPerformed


