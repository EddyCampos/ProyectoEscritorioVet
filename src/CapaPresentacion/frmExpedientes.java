    //Click Boton Guardar
    private void BtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarActionPerformed
        try {
            LExpedientes logica = new LExpedientes();
            
            if (!txtNombre.getText().equals("") &&
            !txtCodigoCliente.getText().equals("") &&
            !txtEspecie.getText().equals("") &&
            !txtRaza.getText().equals("") &&
            !txtPeso.getText().equals("")){
                
                ExpedientesMascotas expediente = GenerarEntidadExpediente();
                
                if (!expediente.isExiste()) {
                    if (logica.InsertarExpediente(expediente) > 0) {
                        JOptionPane.showMessageDialog(this, logica.getMensaje());
                        LimpiarCasillas();
                        ListarExpedientes("");
                    }
                    else{
                        JOptionPane.showMessageDialog(this, logica.getMensaje());
                    }
                }
                else{
                    if (logica.ActualizarExpediente(expediente) > 0) {
                        JOptionPane.showMessageDialog(this, logica.getMensaje());
                        LimpiarCasillas();
                        ListarExpedientes("");
                    }
                    else{
                        JOptionPane.showMessageDialog(this, logica.getMensaje());
                    }
                }
            }
            else{
                JOptionPane.showMessageDialog(this, "Debe completar todos los campos primero");
            }
            //Activa boton buscar cliente
            btnBuscarCliente.setEnabled(true);
        } 
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_BtnGuardarActionPerformed

